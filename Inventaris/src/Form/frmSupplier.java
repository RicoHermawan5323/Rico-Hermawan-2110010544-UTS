    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
        // TODO add your handling code here:
        int jawab = JOptionPane.showConfirmDialog(null, "Apakah yakin menyimpan data?","Konfirmasi",JOptionPane.YES_NO_OPTION);
            if (jawab==0) {
            try {
                st = kon.createStatement();
                if (tKode.getText().equals("") || tNama.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "Data tidak boleh kosong","Validasi",JOptionPane.ERROR_MESSAGE);
                    return;                
                }
                if (bTambah.isEnabled()) {
                    //cek kode
                    String cekkode = "SELECT * FROM supplier WHERE kode='"+tKode.getText()+"'";
                    rs = st.executeQuery(cekkode);
                    if (rs.next()) {
                        JOptionPane.showMessageDialog(null, "Maaf kode sudah ada");                    
                    } else {
                        //insert                     
                        String sql = "INSERT INTO supplier (kode,nama,alamat) VALUES ('"+tKode.getText()+
                                 "','"+tNama.getText()+
                                 "','"+tAlamat.getText()+"')";
                         st.executeUpdate(sql);
                        JOptionPane.showMessageDialog(null, "Berhasil menambah data");
                        Bersih();
                        TampilData();
                    }

                } else if (bUbah.isEnabled()) {
                     //update                     
                    String sql = "UPDATE supplier SET nama = '"+tNama.getText()+
                            "', alamat = '"+tAlamat.getText()+
                            "' WHERE kode = '"+tKode.getText()+"'";
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Berhasil mengubah data");
                    Bersih();
                    TampilData();

                }
            } catch (Exception e) {
            }
        }
            
    }//GEN-LAST:event_bSimpanActionPerformed

